.686P                    ; Директива для процессора Pentium Pro и выше
.MODEL FLAT, C           ; Модель памяти flat (плоская) с соглашением вызовов C
.CODE                    ; Начало сегмента кода

; === ОСНОВНАЯ ФУНКЦИЯ - ОБРАБОТКА ВСЕЙ СТРОКИ ===
; Вход: через стек - указатель на строку и указатель на выходной буфер
; Выход: EAX = 0 (слова были), EAX = 1 (слов нет)
process_all_words PROC
    ; === СОХРАНЕНИЕ РЕГИСТРОВ ===
    push ebp             ; Сохраняем базовый указатель вызывающей функции
    mov ebp, esp         ; Устанавливаем новый базовый указатель на текущую вершину стека
    push esi             ; Сохраняем ESI (будет использоваться для чтения строки)
    push edi             ; Сохраняем EDI (будет использоваться для записи в буфер)
    push ebx             ; Сохраняем EBX (будет использоваться для временного хранения)
    
    ; === ПОЛУЧЕНИЕ ПАРАМЕТРОВ ИЗ СТЕКА ===
    mov esi, [ebp + 8]   ; ESI = адрес начала входной строки (первый параметр)
    mov edi, [ebp + 12]  ; EDI = адрес выходного буфера (второй параметр)
    
    ; === СОХРАНЕНИЕ НАЧАЛА ВЫХОДНОГО БУФЕРА ===
    mov ebx, edi         ; EBX сохраняет начало выходного буфера
    
    ; === ПРОВЕРКА НА ПУСТУЮ СТРОКУ ===
    mov al, [esi]        ; Загружаем первый символ строки в AL
    test al, al          ; Проверяем, не является ли символ нулем (конец строки)
    jz no_words          ; Если ноль - переходим к метке no_words
    cmp al, 0Ah          ; Сравниваем с символом новой строки 
    jz no_words          ; Если новая строка - переходим к метке no_words
    
    ; === ОСНОВНОЙ ЦИКЛ ОБРАБОТКИ ВСЕХ СЛОВ ===
process_loop:
    ; === ИЗВЛЕЧЕНИЕ СЛОВА В ВЫХОДНОЙ БУФЕР ===
    call extract_one_word ; Вызываем подпрограмму извлечения одного слова
    
    ; === ПРОВЕРКА РЕЗУЛЬТАТА ===
    cmp eax, 1           ; Сравниваем результат с 1 (слов нет)
    je process_done      ; Если равно 1 - переходим к завершению обработки
    
    ; === ДОБАВЛЕНИЕ ПЕРЕВОДА СТРОКИ ПОСЛЕ СЛОВА ===
    mov BYTE PTR [edi], 0Ah ; Записываем символ новой строки в текущую позицию буфера
    inc edi               ; Переходим к следующей позиции в буфере
    
    ; === ПРОВЕРКА КОНЦА СТРОКИ ДЛЯ СЛЕДУЮЩЕЙ ИТЕРАЦИИ ===
    mov esi, [ebp + 8]   ; Восстанавливаем указатель на начало входной строки
    mov al, [esi]        ; Загружаем первый символ обновленной строки
    test al, al          ; Проверяем на конец строки
    jz process_done      ; Если конец строки - завершаем обработку
    cmp al, 0Ah          ; Сравниваем с символом новой строки
    jz process_done      ; Если новая строка - завершаем обработку
    
    jmp process_loop     ; Возвращаемся к началу цикла для обработки следующего слова
    
process_done:
    ; === ДОБАВЛЕНИЕ КОНЦА СТРОКИ ===
    mov BYTE PTR [edi], 0 ; Добавляем нулевой терминатор в конец выходной строки
    
    mov eax, 0           ; Устанавливаем код возврата 0 (слова были обработаны)
    jmp cleanup          ; Переходим к очистке

no_words:
    mov eax, 1           ; Устанавливаем код возврата 1 (слов не было)

cleanup:
    pop ebx              ; Восстанавливаем значение EBX
    pop edi              ; Восстанавливаем значение EDI
    pop esi              ; Восстанавливаем значение ESI
    pop ebp              ; Восстанавливаем значение EBP
    ret                  ; Возвращаем управление вызывающей программе
process_all_words ENDP   ; Конец процедуры

; === ПОДПРОГРАММА ИЗВЛЕЧЕНИЯ ОДНОГО СЛОВА ===
; Вход: ESI = указатель на строку, EDI = текущая позиция в выходном буфере
; Выход: EAX = 0 (слово найдено), EAX = 1 (слов нет)
extract_one_word PROC
    ; === ПРОПУСК НАЧАЛЬНЫХ ПРОБЕЛОВ ===
    call skip_whitespace  ; Вызываем функцию пропуска пробелов и переводов строк
    
    ; === ПРОВЕРКА НА КОНЕЦ СТРОКИ ===
    mov al, [esi]        ; Загружаем текущий символ из строки
    test al, al          ; Проверяем на конец строки (ноль)
    jz no_more_words     ; Если конец строки - слов больше нет
    cmp al, 0Ah          ; Сравниваем с символом новой строки
    jz no_more_words     ; Если новая строка - слов больше нет
    
    ; === КОПИРОВАНИЕ СЛОВА В ВЫХОДНОЙ БУФЕР ===
copy_word:
    mov al, [esi]        ; Загружаем текущий символ из строки
    test al, al          ; Проверяем на конец строки
    jz end_copy          ; Если конец строки - завершаем копирование
    cmp al, ' '          ; Сравниваем с пробелом
    je end_copy          ; Если пробел - завершаем копирование
    cmp al, 0Ah          ; Сравниваем с символом новой строки
    je end_copy          ; Если новая строка - завершаем копирование
    
    ; Копирование символа в выходной буфер
    mov [edi], al        ; Копируем символ из строки в буфер
    inc esi              ; Переходим к следующему символу в исходной строке
    inc edi              ; Переходим к следующей позиции в выходном буфере
    jmp copy_word        ; Возвращаемся к началу цикла копирования
    
end_copy:
    ; === ПРОПУСК ПРОБЕЛОВ ПОСЛЕ СЛОВА ===
    call skip_whitespace  ; Пропускаем пробелы после найденного слова
    
    ; === ПЕРЕМЕЩЕНИЕ ОСТАТКА СТРОКИ В НАЧАЛО ВХОДНОГО БУФЕРА ===
    push esi             ; Сохраняем текущую позицию в строке в стек
    push edi             ; Сохраняем текущую позицию в буфере в стек
    
    mov edi, [ebp + 8]   ; EDI = указатель на начало входного буфера
    mov esi, [esp + 4]   ; ESI = текущая позиция в строке после пропуска пробелов
    
copy_remainder:
    mov al, [esi]        ; Загружаем символ из текущей позиции в строке
    mov [edi], al        ; Копируем этот символ в начало буфера
    inc esi              ; Переходим к следующему символу в исходной строке
    inc edi              ; Переходим к следующей позиции в целевом буфере
    test al, al          ; Проверяем, не является ли символ нулем
    jnz copy_remainder   ; Если не ноль - продолжаем копирование
    
    pop edi              ; Восстанавливаем текущую позицию в буфере
    pop esi              ; Восстанавливаем текущую позицию в строке
    
    mov eax, 0           ; Устанавливаем код возврата 0 (слово найдено)
    jmp extract_done     ; Переходим к завершению функции

no_more_words:
    mov eax, 1           ; Устанавливаем код возврата 1 (слов больше нет)

extract_done:
    ret                  ; Возвращаем управление
extract_one_word ENDP    ; Конец процедуры

; === ВСПОМОГАТЕЛЬНАЯ ФУНКЦИЯ - ПРОПУСК ПРОБЕЛЬНЫХ СИМВОЛОВ ===
; Назначение: продвигает указатель ESI до первого непробельного символа
; Вход: ESI - текущая позиция в строке
; Выход: ESI - позиция первого непробельного символа или конца строки
skip_whitespace PROC
skip_loop:
    mov al, [esi]        ; Загружаем текущий символ в AL
    test al, al          ; Проверяем, не является ли символ нулем
    jz whitespace_done   ; Если ноль (конец строки) - выходим
    cmp al, ' '          ; Сравниваем с пробелом
    je skip_char         ; Если пробел - пропускаем его
    cmp al, 0Ah          ; Сравниваем с символом новой строки
    je skip_char         ; Если новая строка - пропускаем его
    jmp whitespace_done  ; Если не пробел и не новая строка - выходим

; === ПРОПУСК ОДНОГО СИМВОЛА ===
skip_char:
    inc esi              ; Перемещаем указатель на следующий символ
    jmp skip_loop        ; Возвращаемся к проверке следующего символа

; === ВЫХОД ИЗ ФУНКЦИИ ===
whitespace_done:
    ret                  ; Возвращаем управление
skip_whitespace ENDP     ; Конец процедуры

END                     ; Конец программы